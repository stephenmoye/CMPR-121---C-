// Song.cpp

#include "Song.h"

Song::Song() // default constructor (no params)
{
	// init with default values
	title = "";
	artist = "";
}
//-----------------------------------------------------------------//
Song::Song(string title, string artist)
{
	title;
	artist;
}
//-----------------------------------------------------------------//
Song::~Song() {}
//-----------------------------------------------------------------//

//-----------------------------------------------------------------//
void Song::setTitle(string title)
{
	this->title = title;
}
//-----------------------------------------------------------------//

//-----------------------------------------------------------------//
void Song::setArtist(string artist)
{
	this->artist = artist;
}
//-----------------------------------------------------------------//

//-----------------------------------------------------------------//
void Song::displaySong()const
{
	cout << "Title:\t" << title << "\nArtist:\t" << artist;
}
//-----------------------------------------------------------------//