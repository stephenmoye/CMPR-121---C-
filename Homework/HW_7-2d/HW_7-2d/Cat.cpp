// Cat.cpp

#include "Cat.h"

//---Default Constructor-------------------------------------------//
Cat::Cat() 
{
	// default values
	name = "";
	weight = 0;
	color = "";
}
//-----------------------------------------------------------------//

//---Destructor----------------------------------------------------//
Cat::~Cat() {}
//-----------------------------------------------------------------//

//---setName function----------------------------------------------//
void Cat::setName(string name)
{
	this->name = name;
}
//-----------------------------------------------------------------//

//---setWeight function--------------------------------------------//
void Cat::setWeight(int weight)
{
	this->weight = weight;
}
//-----------------------------------------------------------------//

//---setColor function---------------------------------------------//
void Cat::setColor(string color)
{
	this->color = color;
}
//-----------------------------------------------------------------//

//---displayInfo function------------------------------------------//
void Cat::displayInfo()
{
	cout << name << " weighs " << weight << " pounds and is " << color << endl;
}
//-----------------------------------------------------------------//

//---meow function-------------------------------------------------//
void Cat::meow()
{
	cout << name << " says:   MEOW!" << endl;
}
//-----------------------------------------------------------------//